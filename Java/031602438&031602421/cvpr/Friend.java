public class Friend {
    String myself;
    String other;

    public String getMyself() {
        return myself;
    }

    public String getOther() {
        return other;
    }

    public void setMyself(String myself) {
        this.myself = myself;
    }

    public void setOther(String other) {
        this.other = other;
    }
    public Friend(String myself,String other){
        this.myself = myself;
        this.other = other;
    }
}
